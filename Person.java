public class Person{
    //Eigenschaften
    private String name;
    private int alter;
    
    //Funktion
    public String getName(){
        return name;
    }
    
    public void setName(String n){
        name = n;
    }
    
    public int getAlter(){
        return alter;
    }
    
    public void setAlter(int a){
        alter = a;
    }
}